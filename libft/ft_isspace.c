#include "libft.h"

int		ft_isspace(char c)
{
	char s[7];
	char *x;

	ft_strcpy(s, " \n\t\r\v\f");
	x = (char *)s;
	while (*x)
		if (c == *x++)
			return (1);
	return (0);
}
